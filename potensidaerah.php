      <img src="ternak.jpg" width="200" height="128" align= "Right" hapace= "50"  alt="SILA 1">
       PENGEMBANGAN SAPI POTONG Sapi Potong merupakan unggulan di sektor Pertanian dan merupakan penyumbang stok daging Nasional. Namun demikian, kemampuan produksi daging sapi dalam negeri masih belum mampu mencukupi kebutuhan nasional. Tingginya kebutuhan daging di dalam negeri yang menimbulkan masalah yaitu tingginya pemotongan sapi betina produktif. Peningkatan jumlah populasi dan kualitas genetik sapi. Indonesia memiliki banyak jenis sapi lokal, diberikan adalah sapi Peranakan Ongole (PO), yang sebagian besar berada di Jawa Timur termasuk di Kota Probolinggo. Pemurnian sapi PO telah dikembangkan oleh Kelompok Tani Bangu Jaya Kelurahan Sumber Taman Kec. Wonoasih Kota Probolinggo.
